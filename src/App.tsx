export default function App() {
  const [, forceUpdate] = useState<undefined | null>();

  console.log('render');

  return (
    <Doughnut
      data={data}
      options={options}
      plugins={[htmlLegendPlugin]}
      onClick={() => forceUpdate(null)}
    />
  );
}
